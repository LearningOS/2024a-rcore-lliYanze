/// semaphore create syscall
pub fn sys_semaphore_create(res_count: usize) -> isize {
    let tid = current_task()
        .unwrap()
        .inner_exclusive_access()
        .res
        .as_ref()
        .unwrap()
        .tid;
    trace!(
        "kernel:pid[{}] tid[{}] sys_semaphore_create",
        current_task().unwrap().process.upgrade().unwrap().getpid(),
        tid
    );
    let process = current_process();
    let mut process_inner = process.inner_exclusive_access();
    let id = if let Some(id) = process_inner
        .semaphore_list
        .iter()
        .enumerate()
        .find(|(_, item)| item.is_none())
        .map(|(id, _)| id)
    {
        process_inner.semaphore_list[id] = Some(Arc::new(Semaphore::new(res_count)));
        id
    } else {
        process_inner
            .semaphore_list
            .push(Some(Arc::new(Semaphore::new(res_count))));
        process_inner.semaphore_list.len() - 1
    };
    process_inner
        .semaphore_deadlock_detect
        .put_resource(id, res_count);
    debug!("{:?}", process_inner.semaphore_deadlock_detect);
    id as isize
}
/// semaphore up syscall
pub fn sys_semaphore_up(sem_id: usize) -> isize {
    let tid = current_task()
        .unwrap()
        .inner_exclusive_access()
        .res
        .as_ref()
        .unwrap()
        .tid;

    let process = current_process();
    let mut process_inner = process.inner_exclusive_access();

    process_inner
        .semaphore_deadlock_detect
        .put_resource(sem_id, 1);
    process_inner
        .semaphore_deadlock_detect
        .deallocate_resource(tid, sem_id, 1);

    let sem = Arc::clone(process_inner.semaphore_list[sem_id].as_ref().unwrap());
    drop(process_inner);
    sem.up();
    0
}
/// semaphore down syscall
pub fn sys_semaphore_down(sem_id: usize) -> isize {
    let tid = current_task()
        .unwrap()
        .inner_exclusive_access()
        .res
        .as_ref()
        .unwrap()
        .tid;
    let process = current_process();
    let mut process_inner = process.inner_exclusive_access();

    process_inner
        .semaphore_deadlock_detect
        .need_num_resource(tid, sem_id, 1);
    if !process_inner.semaphore_deadlock_detect.detect() {
        process_inner
            .semaphore_deadlock_detect
            .release_need_num_resource(tid, sem_id, 1);
        return -0xdead;
    }

    let sem = Arc::clone(process_inner.semaphore_list[sem_id].as_ref().unwrap());
    drop(process_inner);
    sem.down();

    let mut process_inner = process.inner_exclusive_access();
    process_inner
        .semaphore_deadlock_detect
        .consume_resource(sem_id, 1);
    process_inner
        .semaphore_deadlock_detect
        .allocate_resource(tid, sem_id, 1);
    process_inner
        .semaphore_deadlock_detect
        .release_need_num_resource(tid, sem_id, 1);
    0
}
